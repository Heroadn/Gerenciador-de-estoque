package Classes;

public class Sessao {
	
	private String Session_Name;
	private int Session_id;
	private int Session_idade;
	
	public String getName() {
		return Session_Name;
	}
	public void setName(String session_Name) {
		Session_Name = session_Name;
	}
	public int getId() {
		return Session_id;
	}
	public void setId(int session_id) {
		Session_id = session_id;
	}
	public int getIdade() {
		return Session_idade;
	}
	public void setIdade(int session_idade) {
		Session_idade = session_idade;
	}
	
	
}
